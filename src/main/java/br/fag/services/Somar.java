package br.fag.services;

/**
 * Classe que representa a operação de soma.
 */
public class Somar {

    /**
     * Realiza a operação de soma entre dois números de ponto flutuante.
     *
     * @param primeiroValor O primeiro número a ser somado.
     * @param segundoValor  O segundo número a ser somado.
     * @return              Resultado da operação de soma.
     */
    public float operacao(float primeiroValor, float segundoValor) {
        return primeiroValor + segundoValor;
    }
}
