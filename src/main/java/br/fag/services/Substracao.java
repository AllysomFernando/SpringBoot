package br.fag.services;

/**
 * Classe que representa a operação de subtração.
 */
public class Substracao {

    /**
     * Realiza a operação de subtração entre dois números de ponto flutuante.
     *
     * @param primeiroValor O número do qual o segundo número será subtraído.
     * @param segundoValor  O número a ser subtraído do primeiro número.
     * @return              Resultado da operação de subtração.
     */
    public float operacao(float primeiroValor, float segundoValor) {
        return primeiroValor - segundoValor;
    }
}
