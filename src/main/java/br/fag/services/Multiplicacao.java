package br.fag.services;

/**
 * Classe que representa a operação de multiplicação.
 */
public class Multiplicacao {

    /**
     * Realiza a operação de multiplicação entre dois números de ponto flutuante.
     *
     * @param primeiroValor O primeiro fator da multiplicação.
     * @param segundoValor  O segundo fator da multiplicação.
     * @return              Resultado da operação de multiplicação.
     */
    public float operacao(float primeiroValor, float segundoValor) {
        return primeiroValor * segundoValor;
    }
}
